#2.Create a Python program that checks whether a person is eligible to vote (18 years or older) based on their age.
# Take the person's age as input
age = int(input("Enter your age: "))

# Check if the person is 18 years old or older using if condition
if age >= 18:
    print("You are eligible to vote.")
else:
    print("You are not eligible to vote yet.")

#OUTPUT
"""Enter your age: 18
You are eligible to vote."""
